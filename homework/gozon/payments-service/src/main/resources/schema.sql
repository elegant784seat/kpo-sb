DROP TABLE IF EXISTS payment CASCADE;
DROP TABLE IF EXISTS payment_inbox CASCADE;
DROP TABLE IF EXISTS payment_outbox CASCADE;

CREATE TABLE IF NOT EXISTS account(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    balance NUMERIC(19,2) DEFAULT 0,
    name VARCHAR(30) NOT NULL,
    email VARCHAR(50) NOT NULL UNIQUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS payment(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    account_id INTEGER NOT NULL,
    order_id BIGINT NOT NULL,
    status VARCHAR NOT NULL,
    amount NUMERIC(19,2) NOT NULL CHECK(amount > 0),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS payment_outbox(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    payment_id BIGINT NOT NULL,
    event_id VARCHAR NOT NULL UNIQUE,
    payload TEXT NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    processed_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS payment_inbox(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_id VARCHAR NOT NULL UNIQUE,
    payload TEXT NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    processed_at TIMESTAMP
);


CREATE INDEX IF NOT EXISTS idx_account_email ON account(email);
CREATE INDEX IF NOT EXISTS idx_payment_account_id ON payment(account_id);
CREATE INDEX IF NOT EXISTS idx_payment_order_id ON payment(order_id);
CREATE INDEX IF NOT EXISTS idx_payment_outbox_processed ON payment_outbox(processed_at);
CREATE INDEX IF NOT EXISTS idx_payment_inbox_processed ON payment_inbox(processed_at);